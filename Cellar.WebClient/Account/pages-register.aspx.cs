        protected void CreateUser_Click(object sender, EventArgs e)
        {
            var manager = Context.GetOwinContext().GetUserManager<ApplicationUserManager>();
            var signInManager = Context.GetOwinContext().Get<ApplicationSignInManager>();
            var resultado = Context.GetOwinContext().Get<ApplicationUser>();
            
            var user = new ApplicationUser() { UserName = this.emailaddress.Value, Email = this.emailaddress.Value, Name = this.fullname.Value };
            IdentityResult result = manager.Create(user, this.password.Value);
            if (result.Succeeded)
            {
                // Para obtener más información sobre cómo habilitar la confirmación de cuentas y el restablecimiento de contraseña, visite https://go.microsoft.com/fwlink/?LinkID=320771
